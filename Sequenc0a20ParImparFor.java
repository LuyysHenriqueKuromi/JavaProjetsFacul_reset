public class Sequenc0a20ParImparFor {
    public static void main(String[] args) {

        for (int i = 0; i < 21; i++) {
            if (i % 2 == 0) {
                System.out.printf("%d é um número PAR\n",
                i);
            }else {
                System.out.printf("%d é um número ÍMPAR\n",
                i);
            }
        }
    }
}
