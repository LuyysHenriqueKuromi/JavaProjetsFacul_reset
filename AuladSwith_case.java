public class AuladSwith_case {
    public static void main(String[] args) {
        int numero = 1;
        switch (numero) {
            case 0:
                System.out.print("O valor da var numero é ZERO");
                break;
            case 1:
                System.out.print("O valor da var numero é UM");
                break;
            case 2:
                System.out.print("O valor da var numero é DOIS");
                break;
            default:
                System.out.print("O valor da var numero é diferente de ");
                break;
        }
    }
}
